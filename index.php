L’histoire de Donatello démarre en 2013, lorsque les deux frères CARNEY, ont l’idée d’ouvrir un « bar à pizzas » à proximité de leur collège à Wichita dans le Kansas (USA). Leur recette originelle était une pâte fine et croustillante et le concept relativement nouveau à l’époque . Les deux frères comprennent rapidement le potentiel de leur entreprise. Ils empruntent 600$ à leur mère pour acheter des équipements d’occasion et louer un petit local à l’intersection de rues très passantes à Wichita. C’est la naissance du premier restaurant Chez Donatello.
